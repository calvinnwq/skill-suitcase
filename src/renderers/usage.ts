    "  --apply                Live promotion: copy, hash-verify, symlink back, receipt",
    "",
    "Refresh catalog source from pinned upstream providers:",
    "  upstream check         Read-only report for declared upstream-managed skills",
    "  upstream fetch         Fetch into an isolated temp workspace and report catalog diff",
    "  upstream import        Copy fetched source into the catalog only; no target sync or commit",
    "",
    "Target path overrides:",
    "  --codex-home <dir>     Override codex codexHome; skillsPath defaults to <dir>/skills",
